DATA SEGMENT 
	MSG1 DB 'ENTER THE FIRST NUMBER (DIVIDEND) : $'
	MSG2 DB 0AH,0DH,'ENTER THE SECOND NUMBER (DIVISOR) : $'
	MSG3 DB 0AH,0DH,'THE QUOTIENT IS= $'
	MSG4 DB 0AH,0DH,'THE REMAINDER IS = $'
	NUM1 DB ?
	NUM2 DB ?
	QNT DB ?
	REM DB ?
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START: MOV AX,DATA
	MOV DS,AX
	
	LEA DX,MSG1
	MOV AH,09H
	INT 21H
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV NUM1,AL
	
	LEA DX,MSG2
	MOV AH,09H
	INT 21H
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV NUM2,AL
	
	MOV AL,NUM1
	MOV AH,00H
	MOV BL,NUM2
	DIV BL
	
	ADD AL,30H
	MOV QNT,AL
	ADD AH,30H
	MOV REM,AH
	
	LEA DX,MSG3
	MOV AH,09H
	INT 21H
	
	MOV DL,QNT
	MOV AH,02H
	INT 21H
	
	LEA DX,MSG4
	MOV AH,09H
	INT 21H
	
	MOV DL,REM
	MOV AH,02H
	INT 21H
	
	MOV AH,4CH
	INT 21H
CODE ENDS
END START


